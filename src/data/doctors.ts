// ── Helpers ───────────────────────────────────────────────────────────────────

export function getDoctorBySlug(slug: string): DoctorData | undefined {
  return doctors.find((d) => d.slug === slug);
}

export function getDoctorsByDepartment(departmentSlug: string): DoctorData[] {
  return doctors.filter((d) => d.departmentSlugs.includes(departmentSlug));
}

export function getDoctorsByLocation(locationSlug: string): DoctorData[] {
  return doctors.filter((d) => d.locationSlugs.includes(locationSlug));
}

